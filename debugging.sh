# Making notes for shortcuts 

Ctrl + k # Delete everything from the cursor to the end of the line. 
Ctrl + u # Delete everything from the cursor to the start of the line. 
Ctrl + w # Delete one word from the cursor. 
Ctrl + r # Search for history 
Ctrl + l # clear the screen
Ctrl + s # Stop the output
Ctrl + q # enable the output to the screen
Ctrl + c # Terminate command 
Ctrl + z # Throw current command 
